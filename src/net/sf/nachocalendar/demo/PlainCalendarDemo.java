    private void bCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCalcActionPerformed
        Date c1 = (Date) d1.getValue();
        Date c2 = (Date) d2.getValue();
        if ((c1 != null) && (c2 != null)) {
            long t = c2.getTime() - c1.getTime();
            if (t < 0) t *= -1;
            int d = (int) (t / (3600000 * 24));
            tResult.setText("Time Difference: " + d + " days");
        } else tResult.setText("");
    }//GEN-LAST:event_bCalcActionPerformed

